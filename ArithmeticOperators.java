public class ArithmeticOperators{
	public static void main(String args[]){
		int a=7;
		int b=2;
		System.out.println("a value:"+a);
		System.out.println("b value:"+b);
		System.out.println("a+b="+(a+b));
		System.out.println("a value:"+a);
		System.out.println("b value:"+b);
		System.out.println("a-b="+(a-b));
		System.out.println("a value:"+a);
		System.out.println("b value:"+b);
		System.out.println("a*b="+(a*b));
		System.out.println("a value:"+a);
		System.out.println("b value:"+b);
		System.out.println("a/b="+(a/b));
		System.out.println("a value:"+a);
		System.out.println("b value:"+b);
		System.out.println("a%b="+(a%b));
	}
}